  // ─── KPIs rápidos para Dashboard ──────────────────────────────────────────
  static int get totalDocumentos => documentos.length;

  static int get documentosProcesados => documentos
      .where((d) =>
          d.estatus == 'extraido' ||
          d.estatus == 'revisado' ||
          d.estatus == 'archivado')
      .length;

  static int get documentosPendientes => documentos
      .where((d) => d.estatus == 'pendiente' || d.estatus == 'procesando')
      .length;

  static int get documentosRechazados =>
      documentos.where((d) => d.estatus == 'rechazado').length;

  static double get confianzaPromedio {
    final procesados = documentos.where((d) => d.confianzaIA > 0).toList();
    if (procesados.isEmpty) return 0;
    return procesados.fold(0.0, (s, d) => s + d.confianzaIA) /
        procesados.length;
  }

